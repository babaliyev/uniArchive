    ,in_DESCRIPTION            IN ARCH.ADDRESS_DICTIONARY.DESCRIPTION%TYPE
    ) is
  begin
    INS_ADDRESS_DICTIONARY_WI(
                                ADDRESS_DICTIONARY_SEQ.NEXTVAL
                                ,in_PARENT_ID
                                ,in_NAME
                                ,in_DESCRIPTION
                                );
