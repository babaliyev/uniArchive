      )
    VALUES
      (
      in_id,
       in_DOCUMENT_ID
      ,in_COPY_TYPE_ID
      ,in_NAME
      ,in_PAGES_COUNT
      ,in_FULL_ACCESS
      );
  END INS_COPIES_WI;

 
  PROCEDURE INS_COPIES
    (
     in_DOCUMENT_ID            IN ARCH.COPIES.DOCUMENT_ID%TYPE
