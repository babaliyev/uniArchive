    ,in_NAME                   IN ARCH.COPIES.NAME%TYPE
    ,in_PAGES_COUNT            IN ARCH.COPIES.PAGES_COUNT%TYPE
    ,in_FULL_ACCESS            IN ARCH.COPIES.FULL_ACCESS%TYPE
    ) IS
  BEGIN
    INS_COPIES_WI(
    COPIES_SEQ.NEXTVAL,
       in_DOCUMENT_ID
      ,in_COPY_TYPE_ID
      ,in_NAME
      ,in_PAGES_COUNT
      ,in_FULL_ACCESS
      );
  END INS_COPIES;

